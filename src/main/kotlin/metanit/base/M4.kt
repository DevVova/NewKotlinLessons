package metanit.base

const val PI = 3.14//Пример объявления констант. Их объявляют вне функций и классов, на самом верхнем уровне.

fun main() {
    val a: Double//Это пример неизменяемой переменной. Здесь мы её только объявили.
    var b = 9 * PI.toInt()//Это пример изменяемой переменной. Её объявили и сразу инициализировали(указали значение).
    a = b.toDouble()//Здесь переменной объявленной ранее присвоили значение.
    b = 78
    println("a = $a, b = $b")
}